/**
 * Returns whether or not the matrices have exactly the same elements in the same position (when compared with ===)
 *
 * @param {mat4} a The first matrix.
 * @param {mat4} b The second matrix.
 * @returns {Boolean} True if the matrices are equal, false otherwise.
 */
int mat4_exactEquals(float *a, float *b) {
  return a[0] == b[0] && a[1] == b[1] && a[2] == b[2] && a[3] == b[3] &&
         a[4] == b[4] && a[5] == b[5] && a[6] == b[6] && a[7] == b[7] &&
         a[8] == b[8] && a[9] == b[9] && a[10] == b[10] && a[11] == b[11] &&
         a[12] == b[12] && a[13] == b[13] && a[14] == b[14] && a[15] == b[15];
};

/**
 * Returns whether or not the matrices have approximately the same elements in the same position.
 *
 * @param {mat4} a The first matrix.
 * @param {mat4} b The second matrix.
 * @returns {Boolean} True if the matrices are equal, false otherwise.
 */
int mat4_equals(float *a, float *b) {
  float a0 = a[0], a1 = a[1], a2 = a[2], a3 = a[3];
  float a4 = a[4], a5 = a[5], a6 = a[6], a7 = a[7];
  float a8 = a[8], a9 = a[9], a10 = a[10], a11 = a[11];
  float a12 = a[12], a13 = a[13], a14 = a[14], a15 = a[15];

  float b0 = b[0], b1 = b[1], b2 = b[2], b3 = b[3];
  float b4 = b[4], b5 = b[5], b6 = b[6], b7 = b[7];
  float b8 = b[8], b9 = b[9], b10 = b[10], b11 = b[11];
  float b12 = b[12], b13 = b[13], b14 = b[14], b15 = b[15];

  return (abs(a0 - b0) <= EPSILON * MAX(1.0, MAX(abs(a0), abs(b0))) &&
          abs(a1 - b1) <= EPSILON * MAX(1.0, MAX(abs(a1), abs(b1))) &&
          abs(a2 - b2) <= EPSILON * MAX(1.0, MAX(abs(a2), abs(b2))) &&
          abs(a3 - b3) <= EPSILON * MAX(1.0, MAX(abs(a3), abs(b3))) &&
          abs(a4 - b4) <= EPSILON * MAX(1.0, MAX(abs(a4), abs(b4))) &&
          abs(a5 - b5) <= EPSILON * MAX(1.0, MAX(abs(a5), abs(b5))) &&
          abs(a6 - b6) <= EPSILON * MAX(1.0, MAX(abs(a6), abs(b6))) &&
          abs(a7 - b7) <= EPSILON * MAX(1.0, MAX(abs(a7), abs(b7))) &&
          abs(a8 - b8) <= EPSILON * MAX(1.0, MAX(abs(a8), abs(b8))) &&
          abs(a9 - b9) <= EPSILON * MAX(1.0, MAX(abs(a9), abs(b9))) &&
          abs(a10 - b10) <= EPSILON * MAX(1.0, MAX(abs(a10), abs(b10))) &&
          abs(a11 - b11) <= EPSILON * MAX(1.0, MAX(abs(a11), abs(b11))) &&
          abs(a12 - b12) <= EPSILON * MAX(1.0, MAX(abs(a12), abs(b12))) &&
          abs(a13 - b13) <= EPSILON * MAX(1.0, MAX(abs(a13), abs(b13))) &&
          abs(a14 - b14) <= EPSILON * MAX(1.0, MAX(abs(a14), abs(b14))) &&
          abs(a15 - b15) <= EPSILON * MAX(1.0, MAX(abs(a15), abs(b15))));
};

